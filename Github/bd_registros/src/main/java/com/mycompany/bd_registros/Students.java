    private void verificarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verificarBtnActionPerformed
        // TODO add your handling code here:
        try{
            Connection conexion = conectar.conectar();
            PreparedStatement seleccionar = conexion.prepareStatement("SELECT * FROM estudiantes");
            
            ResultSet consulta = seleccionar.executeQuery();
            
            if(consulta.next()) {
                JOptionPane.showMessageDialog(null, "La base de datos tiene datos");
                conectar.cerrarConexion();
            }else {
                JOptionPane.showMessageDialog(null, "La base de datos no tiene datos");
            }
            
            conectar.cerrarConexion();
            
        }catch(Exception error){
            JOptionPane.showMessageDialog(null, error);
        }
    }//GEN-LAST:event_verificarBtnActionPerformed

    private void buscarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBtnActionPerformed
        // TODO add your handling code here:
        
        consultaTxt.setText("");
        
        try{
        
            Connection conexion = conectar.conectar();
            PreparedStatement seleccionar = conexion.prepareStatement("SELECT * FROM estudiantes");
            ResultSet consulta = seleccionar.executeQuery();
            
            while(consulta.next()) {
                consultaTxt.append(consulta.getString(1));
                consultaTxt.append("    ");
                consultaTxt.append(consulta.getString(2));
                consultaTxt.append("    ");
                consultaTxt.append(consulta.getString(3));
                consultaTxt.append("    ");
                consultaTxt.append(consulta.getString(4));
                consultaTxt.append("    ");
                consultaTxt.append(consulta.getString(5));
                consultaTxt.append("    ");
                consultaTxt.append(consulta.getString(6));
                consultaTxt.append("\n");
            }
            
            conectar.cerrarConexion();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: " +e);
        }
    }//GEN-LAST:event_buscarBtnActionPerformed

