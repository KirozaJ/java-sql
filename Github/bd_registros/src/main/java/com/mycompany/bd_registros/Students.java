    private void modBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modBtnActionPerformed
        // TODO add your  code here:
        try{
            
            Connection conexion = conectar.conectar();
            
            String ID = txt_buscar.getText().trim();
            PreparedStatement modificar = conexion.prepareStatement("UPDATE estudiantes SET nombre = ?, apellido = ?, edad = ?, estado = ?, estado_civil = ? WHERE id_estudiante = " +ID);
            
            modificar.setString(1, nomTxt.getText().trim());
            modificar.setString(2, apeTxt.getText().trim());
            modificar.setString(3, edadTxt.getText().trim());
            modificar.setString(4, estTxt.getSelectedItem().toString());
            modificar.setString(5, civTxt.getSelectedItem().toString());
            modificar.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Estudiante modificado correctamente");
            
            conectar.cerrarConexion();
            
        }catch(Exception error){
            JOptionPane.showMessageDialog(null, "Error: " +error);
        }
        
    }//GEN-LAST:event_modBtnActionPerformed

